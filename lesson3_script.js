// Change innerHTML to update text on the page
function showInnerHTML() {
    document.getElementById("output").innerHTML = "This text was changed using innerHTML!";
}

// Use document.write() (Only for demonstration, not recommended in practice)
function useDocumentWrite() {
    document.write("This was written using document.write(). Warning: This will overwrite the page!");
}

// Show an alert popup
function useAlert() {
    alert("This is an alert box using JavaScript!");
}

// Log a message to the console
function useConsoleLog() {
    console.log("This message was logged using console.log()");
}
